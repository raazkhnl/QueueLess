interface I18nState {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: string) => string;
}

export const useI18n = create<I18nState>((set, get) => ({
  lang: (localStorage.getItem('ql_lang') as Lang) || 'en',
  setLang: (lang) => { localStorage.setItem('ql_lang', lang); set({ lang }); },
  t: (key: string) => {
    const { lang } = get();
    // If Nepali selected, use Nepali first, fallback to English
    if (lang === 'ne') return ne[key] || en[key] || key;
    return en[key] || key;
  },
}));
